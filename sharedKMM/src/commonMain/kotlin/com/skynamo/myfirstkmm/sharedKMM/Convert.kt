package com.skynamo.myfirstkmm.sharedKMM

class Convert {

    fun ounceToGram(ounces: Double): Double {
        return ounces * 28.3495
    }

    fun ounceToMl(ounces: Double): Double {
        return ounces * 29.5735
    }

    fun poundToGram(pounds: Double): Double {
        return pounds * 453.592
    }
}